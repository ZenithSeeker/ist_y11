def months():
    month = int(input("Enter a whole number 1-12: "))
    if month == 1:
        print("January")
    elif month == 2:
        print("Feb")
    elif month == 3:
        print("Mar")
    elif month == 4:
        print("April")
    elif month == 5:
        print("May")
    elif month == 6:
        print("June")
    elif month == 7:
        print("July")
    elif month == 8:
        print("Aug")
    elif month == 9:
        print("Sep")
    elif month == 10:
        print("oct")
    elif month == 11:
        print("nov")
    else:
        print("dec")

months()